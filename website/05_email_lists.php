        <p>Listed below are answers to the most common questions pertaining to YahooGroups' mailing lists. For complete instructions on using the YahooGroups email lists, consult the <a href="http://help.yahoo.com/help/groups/" title="Instructions for using the email lists on YahooGroups">YahooGroups Help</a>.</p>

        <h2>Subscribing To A Mailing List</h2>

        <ol>
            <li>Locate a group to join. On the front page of YahooGroups, you can browse the Groups directory or search for a group by topic.</li>
            <li>Click on the <b>Join This Group</b> button on front page of any group.</li>
            <li>If you are not signed in, you will be asked to enter your Yahoo ID. If you do not have a Yahoo ID, you can click on the link <b>New to Yahoo? Sign up now</b>.
            </li><li>Set your membership preferences. When you join, you can choose a profile you'd like to display to the group, select the email address at which you'd like to receive group messages, choose how you receive group messages, and more.</li>
        </ol>

        <h2>Posting To A Mailing List</h2>

        <p>When posting to the PCGen lists on YahooGroups, your message will be more likely to attract the attention of the appropriate individuals if you clearly label its subject:</p>

        <ul>
            <li>
                Bugs: The title of the post should start with <b>[BUG]</b>, and the body of the post should contain the version of PCGen, your operating system version, and a step-by-step process to duplicate the bug.
            </li>
            <li>
                Feature Requests: The title of the post should start with <b>[FREQ]</b> (abbreviation for feature request). Specific suggestions or requests are welcomed and appreciated.
            </li>
            <li>
                Documentation: The title of the post should start with <b>[DOC]</b> if you are requesting clarification of documentation or if you have any suggestions about improving the documentation.
            </li>
            <li>
                Output/Character Sheets: The title of the post should start with <b>[OS]</b> if you're commenting about the output sheets and want to make sure that the output sheet programmers notice your post.
            </li>
            <li>
                Web Site: The title of the post should start with <b>[WEB]</b> if you're commenting about the PCGen home page located at <a href="http://pcgen.sourceforge.net/" title="You are looking at it">http://pcgen.sourceforge.net/</a>.
            </li>
        </ul>

        <p>You can post messages to your group by email or from the YahooGroups web site:</p>

        <h3>Via Email</h3>

        <p>To send a message to other group members, create an email and address it to groupname@yahoogroups.com, substituting the actual name of your group for &quot;groupname.&quot; Group names are not case sensitive.</p>

        <p>Note: you can only post to groups you've joined, and only from email addresses registered with YahooGroups. If you misspell the name of a group, or attempt to send an email from an account other than the one you joined under, your message will not be delivered. If you receive a failure notice, check your email addresses and try again.</p>

        <h3>Via the YahooGroups Web Site</h3>

        <p>After you've <a href="http://login.yahoo.com/" title="The YahooGroups sign-in page">signed in</a>, go to the group you wish to post to, click Post, write your message, and then click Send Message.</p>

        <p>Note: YahooGroups restricts messages to 1 megabyte in size to prevent abuse.</p>

        <h2>Managing Your Subscription Preferences</h2>

        <p>As a YahooGroups member, you can customize your individual preferences and manage the email addresses you use for subscriptions. Simply click on the <a href="http://groups.yahoo.com/myprefs/">My Email Preferences</a> link on the My Groups page.</p>

        <p>Note: You must be <a href="http://login.yahoo.com/" title="Sign in now">signed in</a> with your Yahoo ID in order to access your preferences.</p>

        <h2>Unsubscription (Leaving A Mailing List)</h2>

        <p>In order to unsubscribe from a mailing list:</p>

        <ol>
            <li>Sign in to YahooGroups and go to the <a href="http://groups.yahoo.com/mygroups">My Groups</a> page.</li>
            <li>Click on the <a href="http://groups.yahoo.com/mygroups?sort=m&amp;order=a&amp;page=1&amp;edit=1">Edit My Groups</a> link at the top of the page.</li>
            <li>Look for the group you wish to leave, and check the corresponding box on the right side of the page.</li>
            <li>Click <b>Save Changes</b> at the bottom or top of the page.</li>
        </ol>

        <p>The group will no longer be listed on the My Groups page, and you will no longer receive messages.</p>

