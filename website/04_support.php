        <h1>Technical and User-To-User Support</h1>

        <h2>Online Documentation</h2>

        <p>Before asking someone for help, please read <a href="http://www.pcgen-test.org/autobuilds/pcgen-docs/index.html" title="PCGen Documentation">the documentation</a> (this documentation is also included in the full PCGen download archive). There is a very strong chance that your question has already been asked and answered, and that the answer is waiting for you in the PCGen docs. Imagine the pride you will feel at being able to solve your own problem.</p>

        <h2>Technical Support Overview</h2>

        <p>Technical and user-to-user support for PCGen are made possible with tools provided by <a href="https://sourceforge.net/" title="SourceForge.net">SourceForge</a> and <a href="http://groups.yahoo.com/" title="YahooGroups">Yahoo</a>.  Actual items that are being tracked for development such as bugs and feature requests are handled by <a href="http://jira.pcgen.org">JIRA</a>.  You can get support in the following ways:</p>
        <ol>
            <li>For almost every issue that needs attention you can go to one of the appropriate PCGen groups <a href="http://groups.yahoo.com/search?query=pcgen&amp;submit=Search" title="You must log into YahooGroups in order to subscribe to the email lists.">email lists</a>. It is important to send your question to the appropriate mailing list. For example, an issue pertaining to a list file design problem should be directed to the PCGenListFileHelp list, not the PCGenView list.</li>
            <li>Alternatively you can record your issue in the <a href="http://jira.pcgen.org" title="You must log into JIRA in order to submit or monitor issues.">JIRA</a>.</li>
        </ol>

        <h2>Your SourceForge Account</h2>

        <p>Communication is one of the most important components of the Open Source development process. SourceForge's mailing lists, discussion forums, and issue tracking system facilitate direct communication between project developers, source file authors, and end users.</p>

